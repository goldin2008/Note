        st = [root]
        while st:
            node = st.pop()
            if node is None:
                node = st.pop()
                result.append(node.val)
            else:
                if node.right: #右
                    st.append(node.right)
                if node.left: #左
                    st.append(node.left)
                st.append(node) #中
                st.append(None)
        return result
